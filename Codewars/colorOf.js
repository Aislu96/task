//
// var num=111;
// var a=num.toString(),
//     b=num.toLocaleString(),
//     c=num+""
// console.log([a,b,c])   //put them to an array, we can see the quotes
// //output: [ '111', '111', '111' ]
// Мы видим, если вы просто хотите перевести число в строковое представление, то в общем случае результаты, полученные этими способами, одинаковы. Мне нравится использовать последний, потому что я ленив ;-)
