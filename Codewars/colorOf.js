// Задача
// Кодирование в функции colorOf. функция принимает 3 параметра: r g b. Это означает значение цвета красного, зеленого и синего. диапазон значений: 0–255.
//
// Используйте toString(16)Преобразовать числа из RGB в форму шестнадцатеричной строки. наконец, объедините их с кодом веб-цвета и верните его.
//
// цветовой код должен начинаться с «#». а затем используйте 2 символа для каждого цвета.
//
// например:
//
// colorOf(255,0,0) should return "#ff0000"
// colorOf(0,111,0) should return "#006f00"
// colorOf(1, 2 ,3) should return "#010203"
// Это все твоя работа. Моя работа — напечатать ваш цветовой код на вашем экране.
function colorOf(...numbers) {
    console.log(numbers)
    let value = '#';
    for (let i = 0; i < numbers.length; i++) {
        let a = numbers[i].toString(16)
        if (a.length === 1) {
            value += '0';
        }
        value += a;
    }
    return value;
}

colorOf(255,0,0);
colorOf(0,111,0);
colorOf(1,2,3)
