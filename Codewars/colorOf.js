// А toString() объекта Number имеет более специальное использование: в соответствии с базой выводятся разные строки.
//
// NumberObject.toString(radix)
// radixявляется необязательным параметром. если его проигнорировать, вывод будет десятичным по умолчанию. Если нет, то вот так:
